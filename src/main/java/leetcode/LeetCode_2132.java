    public static int sum2(int[][] dp, int r2, int c2, int stampHeight, int stampWidth) {
        int m = dp.length - 1;
        int n = dp[0].length - 1;

        int r = Math.max(0, r2 - stampHeight + 1);
        int c = Math.max(0, c2 - stampWidth + 1);

        return dp[r2 + 1][c2 + 1] - dp[r2 + 1][c] - dp[r][c2 + 1] + dp[r][c];
    }

    public static boolean possibleToStamp_02(int[][] grid, int stampHeight, int stampWidth) {
        int m = grid.length;
        int n = grid[0].length;

        int[][] dp = new int[m + 1][n + 1];
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                grid[i][j] ^= 1;
                dp[i + 1][j + 1] = dp[i + 1][j] + dp[i][j + 1] - dp[i][j] + grid[i][j];
            }
        }

        int[][] new_grid = new int[m][n];

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (grid[i][j] == 1 && sum(dp, i, j, stampHeight, stampWidth) == stampHeight * stampWidth) {
                    new_grid[i][j] = 1;
                }
            }
        }

        int[][] dp2 = new int[m + 1][n + 1];
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                dp2[i + 1][j + 1] = dp2[i + 1][j] + dp2[i][j + 1] - dp2[i][j] + new_grid[i][j];
            }
        }

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (new_grid[i][j] == 1) System.out.println(i + " " + j);
                if (grid[i][j] == 1) {
                    if (sum2(dp2, i, j, stampHeight, stampWidth) <= 0) return false;
                }
            }
        }
        return true;

    }

    public static int sum(int[][] dp, int r, int c, int stampHeight, int stampWidth) {
        int m = dp.length - 1;
        int n = dp[0].length - 1;
        if (r + stampHeight - 1 >= m || c + stampWidth - 1 >= n) return 0;

        int r2 = r + stampHeight - 1;
        int c2 = c + stampWidth - 1;

        return dp[r2 + 1][c2 + 1] - dp[r2 + 1][c] - dp[r][c2 + 1] + dp[r][c];
    }

